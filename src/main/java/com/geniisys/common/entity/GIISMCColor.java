	/** The color cd. */
	private Integer colorCd;
	
	/** The color. */
	private String color;
	
	/** The remarks. */
	private String remarks;
	
	/** The cpi rec no. */
	private String cpiRecNo;
	
	/** The cpi branch cd. */
